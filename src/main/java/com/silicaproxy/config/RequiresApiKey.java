package com.silicaproxy.config;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

// Marks a controller method as requiring a Bearer API key matching the given scope.
// Read by ApiKeyInterceptor via reflection on the resolved HandlerMethod, so it must stay
// available at runtime.
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface RequiresApiKey {
    ApiKeyScope value();
}
